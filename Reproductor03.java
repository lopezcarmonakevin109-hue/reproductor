        private void reproducirMusica() {
        if (archivoMusica == null) {
            JOptionPane.showMessageDialog(this, "Primero carga un archivo MP3.");
            return;
        }

        hiloReproduccion = new Thread(() -> {
            try (FileInputStream fis = new FileInputStream(archivoMusica)) {
                player = new Player(fis);
                player.play();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al reproducir: " + ex.getMessage());
            }
        });
        hiloReproduccion.start();
    }

  



